        gDisableAnimatedPalette = -0x1;

        // Check is in stealth
        if (!EventFunction(EVENT_ACTION_CHECKING, EVENT_FULLY_POWERED_SUIT_OBTAINED))
            MusicUpdateAfterAlarmDisable();
